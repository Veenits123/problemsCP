const int N = 1e5 + 5;

class Solution {
public:
	int fib(int N) {
		if (N <= 1)
			return N;

		int a = 0, b = 1;
		for (int i = 2; i <= N; i++) {
			int temp = a + b;
			a = b;
			b = temp;
		}
		return b;
	}
};

void solve() {

	int q; cin >> q;
	while (q--) {
		int n; cin >> n;
		cout << Solution().fib(n) << endl;
	}

	return ;
}

