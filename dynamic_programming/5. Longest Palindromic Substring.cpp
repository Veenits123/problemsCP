const int N = 1e5 + 5;

class Solution {
public:
	string longestPalindrome(string s) {
		int n = s.size();

		int dp[n + 1][n + 1];

		for (int j = 0; j <= n; j++)
			dp[0][j] = dp[1][j] = 1;

		int len = 1, en = 1;

		for (int i = 2; i <= n; i++) {
			for (int j = i; j <= n; j++) {
				if (dp[i - 2][j - 1] == 1 && (s[j - 1] == s[j - i])) {
					dp[i][j] = 1;
					len = i, en = j;
				}
				else
					dp[i][j] = 0;
			}
		}
		string ans = s.substr(en - len, len);
		return ans;
	}
};

