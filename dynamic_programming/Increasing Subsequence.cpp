//O(nlogn)
int lis(int a[], int n) {
	vector <int> res;
	res.push_back(a[0]);
	for (int i = 1; i < n; i++) {
		if (a[i] > res.back())
			res.push_back(a[i]);
		else {
			int x = lower_bound(res.begin(), res.end(), a[i]) - res.begin();
			res[x] = a[i];
		}
	}
	return res.size();
}

