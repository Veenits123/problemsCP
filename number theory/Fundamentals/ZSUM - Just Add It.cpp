const int mod = 1e7 + 7;

int binpow(int a, int b) {
	int res = 1;
	while (b) {
		if (b & 1)
			res = (res * a) % mod;
		a = (a * a) % mod;
		b >>= 1;
	}
	return res;
}

void solve() {

	while (true) {
		int n, k; cin >> n >> k;
		if (n == 0 && k == 0)
			return ;
		int sn = 2 * (binpow(n - 1, k) % mod) % mod + binpow(n, k) % mod;
		int pn = 2 * (binpow(n - 1, n - 1) % mod) % mod + binpow(n, n) % mod;
		int ans = sn + pn;
		ans %= mod;
		cout << ans << endl;
	}

	return ;
}

