vi ans(N);

void sieve() {
	for (int i = 2; i <= N; i++) {
		if (!is_prime[i]) {
			//fn[i] = i;//if doing this we can run 'j' from 'i*i';
			for (int j = i; j <= N; j += i) {
				if (!fn[j])
					fn[j] = i;
				is_prime[j] = true;
			}
		}
		ans[i] = ans[i - 1] + fn[i];
	}
}

void solve() {

	int n; cin >> n;
	cout << ans[n] << endl;

	return ;
}

