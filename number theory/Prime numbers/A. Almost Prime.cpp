const int N = 3e3 + 5;

bool almost_prime(int n) {

	int two = 0;
	for (int i = 2; i * i <= N; i++) {
		if (n % i == 0) {
			while (n % i == 0) {
				n /= i;
			}
			two++;
		}
	}
	if (n > 1)
		two++;

	return two == 2;
}

void solve() {

	int n; cin >> n;
	int ans = 0;
	for (int i = 1; i <= n; i++) {
		if (almost_prime(i)) {
			//cout << i << " ";
			ans++;
		}
	}
	cout << ans << endl;

	return ;
}

