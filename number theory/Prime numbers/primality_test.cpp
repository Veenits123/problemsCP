bool is_prime(int n) {
	if (n == 1)
		return false;
	for (int i = 2; i * i <= n; i++) {
		if (n % i == 0)
			return false;
	}
	return true;
}

void solve() {
	int n; cin >> n;
	if (is_prime(n))
		cout << "YES" << endl;
	else
		cout << "NO" << endl;

	return ;
}

