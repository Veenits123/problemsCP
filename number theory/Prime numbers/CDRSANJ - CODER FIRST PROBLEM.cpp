bool is_prime(int n) {
	if (n == 1 || n == 0 || n % 2 == 0)
		return false;
	if (n == 2)
		return true;
	for (int i = 3; i * i <= n; i += 2) {
		if (n % i == 0)
			return false;
	}
	return true;
}

