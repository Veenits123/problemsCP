int etf(int n) {
	int cnt = n;
	for (int i = 2; i * i <= n; i++) {
		if (n % i == 0) {
			cnt /= i;
			cnt *= (i - 1);
			while (n % i == 0)
				n /= i;
		}
	}
	if (n > 1) {
		cnt /= n;
		cnt *= (n - 1);
	}
	return cnt;
}

