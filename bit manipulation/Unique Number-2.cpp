class Solution {
public:
	vector<int> singleNumber(vector<int>& nums) {
		int n = nums.size();
		vector <int> ans(2, 0);
		int sum = 0;
		for (int i = 0; i < n; i++)
			sum ^= nums[i];
		sum = sum & (-sum);
		for (int i = 0; i < n; i++) {
			if (sum & nums[i])
				ans[0] ^= nums[i];
			else
				ans[1] ^= nums[i];
		}
		return ans;
	}
};

