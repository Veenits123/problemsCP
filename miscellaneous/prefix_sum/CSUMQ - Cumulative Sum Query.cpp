void solve() {
	int n; cin >> n;
	int a[n];
	for (int i = 0; i < n; i++)
		cin >> a[i];
	int prefix[n];
	prefix[0] = a[0];
	for (int i = 1; i < n; i++)
		prefix[i] = prefix[i - 1] + a[i];
	int q; cin >> q;
	while (q--) {
		int i, j; cin >> i >> j;
		if (i - 1 >= 0)
			cout << prefix[j] - prefix[i - 1] << endl;
		else
			cout << prefix[j] << endl;
	}
}

