bool is_square(int n) {
	int l = 0, r = n;
	while (l <= r) {
		int mid = l + (r - l) / 2;
		if (mid * mid == n)
			return true;
		else if (mid * mid > n)
			r = mid - 1;
		else
			l = mid + 1;
	}
	return false;
}

